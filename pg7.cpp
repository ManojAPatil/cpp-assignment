//prog. to display various type of arithmetic operation using mixed data type
#include<iostream>
using namespace std;

int main()
{
	int a=5,b=7;
	double c=3.7,d=8.0;
	cout<<a<<"+"<<b<<"="<<a+b<<"\n";
	cout<<c<<"+"<<d<<"="<<c+d<<"\n";
	cout<<a<<"+"<<d<<"="<<a+d<<"\n";
	cout<<a<<"-"<<b<<"="<<a-b<<"\n";
	cout<<c<<"-"<<d<<"="<<c-d<<"\n";
	cout<<a<<"-"<<d<<"="<<a-d<<"\n";
	cout<<a<<"*"<<b<<"="<<a*b<<"\n";
	cout<<c<<"*"<<d<<"="<<c*d<<"\n";
	cout<<a<<"*"<<d<<"="<<a*d<<"\n";
	cout<<a<<"/"<<b<<"="<<a/b<<"\n";
	cout<<c<<"/"<<d<<"="<<c/d<<"\n";
	cout<<a<<"/"<<d<<"="<<a/d<<"\n";
	return 0;
	
	
}
